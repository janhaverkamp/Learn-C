//Ausgabe von Bytegröße jeweiliger Datentypen

#include <iostream>
using namespace std;

int main()
{
  cout << "Size of int:"      << sizeof(int)   << endl
       << "Size of short:"    << sizeof(short) << endl
       << "Size of long:"     << sizeof(long)  << endl
       << "Size of double:"   << sizeof(double)<< endl;
  return 0;
}
